struct DSU{
    vector<int> x;
    vector<int> sz;
    DSU(int n){
        x = vector<int> (n);
        sz = vector<int> (n);
        for(int i=0;i<n;i++) x[i] = i,sz[i]=1;
    }
    // 0 index ?
    int find(int u){
        if (x[u]==u) return u;
        return x[u] = find(x[u]);
    }
    void join(int a,int b){
        int ra = find(a);
        int rb = find(b);
        if (sz[ra]<sz[rb]) swap(ra,rb);
        x[rb] = ra;
        if(ra!=rb) sz[ra]+=sz[rb];
    }
};
 

