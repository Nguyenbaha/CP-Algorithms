	// nếu 11 xuất hiện trước "00" thì không có
	void solve()
{
	string s;
	
	cin >> s ;

	int n = s.size() ;

	if(n <= 3) {
		yes ; return ;
	}

	int one = count(all(s),'1') ;

	int zero = count(all(s),'0');

	if(!one || ! zero)
	{
		yes;

		return ;
	}

	auto l = s.find("11") ;
	auto r = s.rfind("00") ; // vị trí xuất hiện cuối cùng của 00
	cout << (l < r && l != -1 && r!=-1 ? "NO" : "YES" );
	cout << endl ;

}	


