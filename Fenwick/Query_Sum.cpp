void updatePoint(int i, int v)
{
    while(i <= n)
    {
        fw[i] += v;
        i += i &(-i);
    }

}
void updateRange(int l, int r, int v)
{
    updatePoint(l,v);
    updatePoint(r+1,-v);
}
int getSum(int i)
{
    int ans  = 0;
    while( i > 0)
    {
        ans += fw[i];
        i -= i&(-i);
    }
    return ans;
}
