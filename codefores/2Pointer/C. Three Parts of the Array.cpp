void never_give_up()
{
   int n;
   vector <int> a;
   cin >> n;
   a.resize(n);
   set<int> s;
   int ans =0 ;
   int l=0,r=0;
   for(auto &i: a)
    cin >> i, l+=i, s.insert(l);

    for(int i = n-1; i>=0; --i)
    {
        s.erase(l);
        l -= a[i];
        r += a[i];
        if(s.count(r))
            ans = max(ans,r);
    }
    cout << ans;
}

