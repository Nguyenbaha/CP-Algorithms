void unstoppable()
{
   int n;
   cin >> n;
   string s;
    cin>> s;
    vector<int> ans;
    int ok=0;
    for(int i=1;i < s.size(); ++i)
        if(s[i] < s[i-1])
            ok = 1;
    if(!ok)
    {
        cout<<s<<endl;
        return;
    }
    for(int i=0; i < s.size(); ++i)
    {
        if(s[i] == '1')
            break;
        ans.push_back(0);
    }
    ans.push_back(0);
    for(int i=n-1;i >=0; --i)
    {
        if(s[i] == '0')
            break;
        ans.push_back(1);
    }
    for(auto e: ans)
        cout << e;
    cout<<endl;
}

