void never_give_up()
{
   int n;
   int dp[N]={0};
   cin >> n;
   int a[n+1];
   for(int i=1;i <=n; ++i)
    cin>> a[i];
    sort(a+1,a+1+n);
   dp[0] = 1;
   dp[1] = 1;
   for( int i=1; i <= n;++i)
   {
       dp[i] = 1;
       for ( int j = 1; j < i; ++ j)
       {
           if(!(a[i]%a[j]) && dp[i] < dp[j]+1)
            dp[i] = dp[j] + 1;
       }
   }
   cout<< *max_element(dp+1,dp+1+n);
}

